print ("Vamos a hacer un premedio para un alumno \n Para eso, ingresa sus notas")
practica=float(input("ingrese las notas de las practicas: "))
participacion=float(input("ingrese las notas de las participacion: "))
examen=float(input("ingrese las notas de los examenes: "))
promedio=(practica+participacion+examen)/3
print (f"Este es tu promedio: {promedio}")